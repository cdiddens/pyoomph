  // Represents one compiled equation code, i.e. one shared library generated by the JIT code
  // generator (GiNaC symbolic equations -> C code -> compiled .so) for a single FiniteElementCode
  // (a bulk domain or an interface). A single DynamicBulkElementCode can be instantiated (via
  // factory_instance) on multiple meshes/domains, each giving a DynamicBulkElementInstance.
  class DynamicBulkElementCode
  {
  protected:
    friend class Problem;
    friend class DynamicBulkElementInstance;
    Problem *problem;
    CCompiler *compiler;
    std::string filename;
    JITFuncSpec_Table_FiniteElement_t *functable; // Function table exported by the compiled .so, containing all residual/jacobian/etc. callbacks
    FiniteElementCode *element_class; // The symbolic (GiNaC-based) code generator this binary was compiled from
    std::map<std::string, int> integral_function_map,extremum_function_map; // Name -> index lookup for integral/extremum diagnostic functions exposed by the code
    void *so_handle; // dlopen() handle of the loaded shared library

  public:
    const std::string &get_file_name() const { return filename; }
    DynamicBulkElementCode(Problem *prob, CCompiler *ccompiler, std::string fnam, FiniteElementCode *elem);
    virtual ~DynamicBulkElementCode(); // Closes the shared library handle
    // Creates a new instance of this code bound to a specific bulk mesh (fields, external data links, etc. are then resolved per-instance)
    DynamicBulkElementInstance *factory_instance(pyoomph::Mesh *bulkmesh);
    const JITFuncSpec_Table_FiniteElement_t *get_func_table() const { return functable; }
    JITFuncSpec_Table_FiniteElement_t *get_func_table() { return functable; }
    int get_max_dt_order() const { return get_func_table()->max_dt_order; }
    int get_integral_function_index(std::string n); // -1 if not present
    int get_extremum_function_index(std::string n); // -1 if not present
    unsigned _set_solved_residual(std::string name);
    FiniteElementCode *get_code() { return element_class; }
  };

